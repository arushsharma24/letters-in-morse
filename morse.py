Morse_Code = { 'a':'.-', 'b':'-...', 'c':'-.-.', 'd':'-..', 'e':'.', 
	      'f':'..-.', 'g':'--.', 'h':'....', 'i':'..', 'j':'.---',
	      'k':'-.-', 'l':'.-..', 'm':'--', 'n':'-.', 'o':'---',
	      'p':'.--.', 'q':'--.-', 'r':'.-.', 's':'...', 't':'-',
	      'u':'..-', 'v':'...-', 'w':'.--', 'x':'-..-', 'y':'-.--',
	      'z':'--..', '1':'.----', '2':'..---', '3':'...--', 
	      '4':'....-', '5':'.....', '6':'-....', '7':'--...', 
	      '8':'---..', '9':'----.', '0':'-----', ', ':'--..--',
	      '.':'.-.-.-', '?':'..--..', '/':'-..-.', '-':'-....-', 
	      '(':'-.--.', ')':'-.--.-', ' ':'  '} 

def encrypt(message): 
	cipher = ' '
	for letter in message:
		if letter != '  ':
			cipher += Morse_Code[letter] + '  '
		else:
			cipher += ' '
	return cipher 

def main(): 
	message = "Hello World"
	result = encrypt(message.lower()) 
	print(result) 

if __name__ == '__main__': 
	main() 
